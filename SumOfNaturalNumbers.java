import java.util.*;
public class SumOfNaturalNumbers {
//By using a loop, find the sum of all numbers from 1 - 100.

    public static void main(String[] args) {

        int sum = 0;
//
        for(int i=1; i<=100; i++ ){
//           System.out.println(sum += i);
            sum += i;
        }
        System.out.println("Sum is " + sum);
//
        }
//
    }
